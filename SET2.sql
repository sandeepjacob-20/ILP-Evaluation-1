CREATE PROCEDURE GETDETAILS
	@empid INT,
	@srlno VARCHAR(7) OUT,
	@mfname VARCHAR(15) OUT,
	@mdl VARCHAR(50) OUT,
	@wt DECIMAL(5,2) OUT
AS
BEGIN 
	SELECT @srlno = SerialNumber, @mfname = MfName, @mdl = Model,
	@wt = Weight FROM COMPUTER WHERE EmployeeID = @empid
END

DECLARE @empid INT
DECLARE	@seriallno VARCHAR(7)
DECLARE	@manfname VARCHAR(15)
DECLARE	@model VARCHAR(50)
DECLARE	@weight DECIMAL(5,2)
SET @empid = 2
EXEC GETDETAILS @empid = @empid, @srlno = @seriallno OUT, @mfname = @manfname OUT,
	@mdl = @model OUT, @wt = @weight OUT
SELECT @seriallno AS 'Serial No.', @manfname AS 'Manufacturer Name',
		@model AS 'Model', @weight AS 'Weight'