CREATE TABLE COMPUTER (SerialNumber VARCHAR(7) PRIMARY KEY,
	MfName VARCHAR(15) FOREIGN KEY REFERENCES MANUFACTURER(MfName),
	Model VARCHAR(50),
	Weight DECIMAL(5,2),
	EmployeeID INT FOREIGN KEY REFERENCES EMPLOYEE(EmployeeID)
)

CREATE TABLE EMPLOYEE (EmployeeID INT PRIMARY KEY,
	EmpName VARCHAR(15),
	Phone VARCHAR(10),
	Email VARCHAR(20),
	Area_Code INT
)
CREATE TABLE MANUFACTURER(MfName VARCHAR(15) PRIMARY KEY,
	City VARCHAR(50),
	State VARCHAR(50)
)

